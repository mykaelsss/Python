/**
 * Reverses the given str.
 * - Time: O(?).
 * - Space: O(?).
 * @param {string} str String to be reversed.
 * @returns {string} The given str reversed.
 */
function reverseString(str) {
    var j =[]
    for(var i=str.length; i >=0; i--){
        j.push(str[i]);
    }
    return j.join("");
}

